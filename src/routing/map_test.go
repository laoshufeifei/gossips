package routing

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestAStar(t *testing.T) {
	test := assert.New(t)
	test.Equal(1, 1)

	s := [][]byte{
		//0    1    2    3    4    5    6
		{'-', '-', '-', '-', '-', '-', '-'}, // 0
		{'-', '-', '-', 'x', '-', '-', '-'}, // 1
		{'-', 's', '-', 'x', '-', 'e', '-'}, // 2
		{'-', '-', '-', 'x', '-', '-', '-'}, // 3
		{'-', '-', '-', '-', '-', '-', '-'}, // 4
	}

	m := newGridMap(s, len(s), len(s[0]))
	m.astar()
}

